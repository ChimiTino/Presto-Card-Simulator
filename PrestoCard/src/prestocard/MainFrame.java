    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        int selected = prestoTable.getSelectedRow();

        if (selected == -1) {
            JOptionPane.showMessageDialog(rootPane, "Please select a card to remove");
        } else {
            // Delete from the array list no the array table
            cards.remove(selected);
        }
//       after the item is removed update the list
        updateSystem();
    }//GEN-LAST:event_deleteBtnActionPerformed

    private void tapBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tapBtnActionPerformed
//        DefaultTableModel model = (DefaultTableModel) prestoTable.getModel();

        int selected = prestoTable.getSelectedRow();

        if (selected == -1) {
            JOptionPane.showMessageDialog(rootPane, "Please select a card to tap");
        } else {
            if (cards.get(selected).getBalance() < 2.50) {
                JOptionPane.showMessageDialog(rootPane, cards.get(selected).getName() + " does not have suffecient funds.");

            } else {
                double cardBalance = Cards.tap(cards.get(selected).getBalance());
//          model.setValueAt(cardBalance, selected, 2);
                cards.get(selected).setBalance(cardBalance);
                updateSystem();
            }

        }


    }//GEN-LAST:event_tapBtnActionPerformed

