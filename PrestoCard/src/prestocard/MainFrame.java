    /**
     * Updates the table and table label
     */
    void updateSystem() {
        tableCount.setText("You have " + cards.size() + " card holders.");
        DefaultTableModel model = (DefaultTableModel) prestoTable.getModel();
        model.setRowCount(0);
        //loop through all the cards is the class
        for (int i = 0; i < cards.size(); i++) {

            model.addRow(new Object[]{
                i + 1, cards.get(i).getName(),
                String.format("$%.2f", cards.get(i).getBalance())
            });
        }

    }

