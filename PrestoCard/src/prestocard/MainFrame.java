    private void topUpBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_topUpBtnActionPerformed
        int selected = prestoTable.getSelectedRow();

        if (selected == -1) {
            JOptionPane.showMessageDialog(rootPane, "Please select a card to add funds to");
        } else {
            String topUpName = cards.get(selected).getName();
            addFunds.setLocationRelativeTo(this);
            addFunds.pack();
            addFunds.setVisible(true);
            AddFundLabel.setText("Add funds for " + topUpName);
            cardholder = selected;
        }
        
        


    }//GEN-LAST:event_topUpBtnActionPerformed

