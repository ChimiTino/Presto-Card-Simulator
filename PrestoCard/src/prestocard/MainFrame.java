    private void addCardHolderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addCardHolderActionPerformed
        String name = addName.getText();
        double balance = (double)addBalaceSpinner.getValue();
        if("".equals(name)){
            JOptionPane.showMessageDialog(rootPane, "Please insert a name to add as a card holder");
        }
        else{
            cards.add(new Cards(name, balance));
            updateSystem();
            addCard.setVisible(false);
            addName.setText("");
            addBalaceSpinner.setValue(0.00);
        }
    }//GEN-LAST:event_addCardHolderActionPerformed

